package com.franciscosagardoy;


public class Atmosfera {
    private int idAtmosfera;
    private int humedad;
    private double presion;
    private int visibilidad;
    private int ambienteAscendente;

    public int getIdAtmosfera() {
        return idAtmosfera;
    }

    public void setIdAtmosfera(int idAtmosfera) {
        this.idAtmosfera = idAtmosfera;
    }

    public int getHumedad() {
        return humedad;
    }

    public void setHumedad(int humedad) {
        this.humedad = humedad;
    }

    public double getPresion() {
        return presion;
    }

    public void setPresion(double presion) {
        this.presion = presion;
    }

    public int getVisibilidad() {
        return visibilidad;
    }

    public void setVisibilidad(int visibilidad) {
        this.visibilidad = visibilidad;
    }

    public int getAmbienteAscendente() {
        return ambienteAscendente;
    }

    public void setAmbienteAscendente(int ambienteAscendente) {
        this.ambienteAscendente = ambienteAscendente;
    }

}
